    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        
         try {
            int row = jTable2.getSelectedRow();
            String Table_click = (jTable2.getModel().getValueAt(row, 0).toString());
            String sql = "SELECT * FROM finance_budget_received where event_id='"+Table_click+"'";
            pst=conn.prepareStatement(sql);
            rs= (ResultSet) pst.executeQuery(sql);
            
            if (rs.next()){
            
            String add1 = rs.getString("event_id");
            event_id1.setText(add1);
            
            String add2 = rs.getString("Amount");
            e_amount.setText(add2);
            
            String add3 = rs.getString("payment_date");
            
             try {
                    Date = new SimpleDateFormat("yyyy-MM-dd").parse(add3);
            
                    event_date.setDate(Date);
                } 
                    catch (ParseException ex) {
            
                }
            
            
            
            
            
            
            }
            
            
        } catch (Exception e) {
            
               JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jTable2MouseClicked

