    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        String search = event_id.getText();
        
        try {
            
            String opt = event_id.getText();
            
            String sql = "SELECT event_id,Amount,payment_date from finance_budget_received where event_id LIKE '%"+search+"%'";
            
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            if(!rs.isBeforeFirst()){
            
            showMessageDialog(null, "'"+search+"' Is not a Registered Member");
            
            }
            
            jTable2.setModel(DbUtils.resultSetToTableModel(rs));
            
            
        } catch (Exception e) {
            
            System.out.println(e);
            showMessageDialog(null, "'"+search+"' Is Not a Registered Member");
            
        }
        
      
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

