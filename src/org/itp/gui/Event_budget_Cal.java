    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String event_id = event_id1.getText();
        String Amount = e_amount.getText();
        String payment_date = ((JTextField)event_date.getDateEditor().getUiComponent()).getText();
        
        Connection conn = null;
        PreparedStatement pst = null;
        
        try {
            String sqlQueryString = "INSERT INTO finance_budget_received (event_id,Amount,payment_date) " + "values (?, ?, ?)";
            
            //Connect to DB
            
            conn = DBconnect.connect();
            pst = conn.prepareStatement(sqlQueryString);
            pst.setString(1, event_id);
            pst.setString(2, Amount);
            pst.setString(3, ((JTextField)event_date.getDateEditor().getUiComponent()).getText());
            
            pst.execute();
            
            JOptionPane.showMessageDialog(null,"Record inserted");
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        finally{
            if(pst != null){
                pst = null;
            }
            if(conn != null){
                conn = null;
            }
            
        }
            tableload();
    }//GEN-LAST:event_jButton2ActionPerformed

